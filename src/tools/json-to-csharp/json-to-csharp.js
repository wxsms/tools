import { quicktype, jsonInputForTargetLanguage, InputData } from 'quicktype-core'

/**
 * @param {string} jsonString
 * @param {{ useList?: boolean, namespace?: string, typeName?: string }} [options]
 * @returns {Promise<{ ok: true, code: string, warning?: string } | { ok: false, error: string }>}
 *
 * quicktype-core C# renderer 行为（Task 10 实测，对照
 * node_modules/quicktype-core/dist/language/CSharp/language.js、CSharpRenderer.js、
 * SystemTextJsonCSharpRenderer.js）：
 * - `just-types: true`  只输出 class/struct 声明，不输出 JSON 反序列化代码、helper 与
 *   属性 attribute。注意：与 C++ 不同，C# 的 `justTypes` **不会**关闭 namespace。
 *   SystemTextJsonCSharpRenderer 构造时 `_needNamespaces = _options.features.namespaces`
 *   （与 justTypes 无关），默认 features='complete' 下 namespaces=true，所以
 *   just-types 模式下 namespace 仍会输出。因此这里不需要像 json-to-cpp.js 那样做
 *   namespace 后处理包裹。
 * - `array-type`  enum：`'array'` → `T[]`，`'list'` → `List<T>`。
 *   **C# 默认 `'array'`**（与 Java 的默认 `'list'` 不同！）。本函数 `useList` 默认 false，
 *   与 quicktype C# 默认一致。
 * - `namespace`  StringOption，默认 'QuickType'；just-types 下仍生效。
 *
 * `typeName` 作为 `addSource({ name })` 传入，控制顶层类型名。非合法 C# 标识符时
 * 回退到 'AutoGenerated' 并返回 warning，让 UI 提示用户。
 */
const CSHARP_IDENT_RE = /^[A-Za-z_][A-Za-z0-9_]*$/

export async function jsonToCSharp(jsonString, {
  useList = false,
  namespace = 'QuickType',
  typeName = 'AutoGenerated',
} = {}) {
  if (!jsonString || jsonString.trim() === '') {
    return { ok: true, code: '' }
  }

  let parsed
  try {
    parsed = JSON.parse(jsonString)
  } catch (e) {
    return { ok: false, error: 'JSON 解析失败：' + e.message }
  }

  const trimmedName = (typeName || '').trim()
  const isValidName = trimmedName === '' || CSHARP_IDENT_RE.test(trimmedName)
  const safeName = isValidName ? (trimmedName || 'AutoGenerated') : 'AutoGenerated'
  const warning = isValidName
    ? undefined
    : `结构体名 "${trimmedName}" 不是合法 C# 标识符`

  try {
    const jsonInput = jsonInputForTargetLanguage('csharp')
    await jsonInput.addSource({ name: safeName, samples: [JSON.stringify(parsed)] })
    const inputData = new InputData()
    inputData.addInput(jsonInput)

    const rendererOptions = {
      'just-types': true,
      namespace,
      'array-type': useList ? 'list' : 'array',
    }

    const result = await quicktype({
      lang: 'csharp',
      inputData,
      rendererOptions,
    })
    return { ok: true, code: result.lines.join('\n'), warning }
  } catch (e) {
    return { ok: false, error: '生成失败：' + e.message }
  }
}
