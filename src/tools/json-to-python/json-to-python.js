import { quicktype, jsonInputForTargetLanguage, InputData } from 'quicktype-core'

/**
 * @param {string} jsonString
 * @param {{ allOptional?: boolean, pydantic?: boolean, typeName?: string }} [options]
 * @returns {Promise<{ ok: true, code: string, warning?: string } | { ok: false, error: string }>}
 *
 * quicktype-core Python renderer 实际行为（Task 2 实测）：
 * - `just-types: true`  只输出 class 声明，不输出 runtime JSON 解析代码。
 * - `pydantic-base-model: true`  让类继承 pydantic.BaseModel。
 * - 顶层 `allPropertiesOptional: true`  让单样本字段渲染为 nullable union（T | null）。
 * - `python-version` 默认 3.10，其 `unionOperators: true` 会把 nullable union 渲染为
 *   PEP 604 的 `T | None` 形式，而不是 `Optional[T]`。为了让 `allOptional` 开关产生
 *   用户可识别的 `Optional[...]` 标记，开启 allOptional 时同时把 `python-version`
 *   降为 3.9（`unionOperators: false`），nullable union 即渲染为 `Optional[T]`。
 *
 * `typeName` 作为 `addSource({ name })` 传入，控制顶层类名。非合法 Python 标识符时
 * 回退到 'AutoGenerated' 并返回 warning，让 UI 提示用户。
 */
const PY_IDENT_RE = /^[A-Za-z_][A-Za-z0-9_]*$/

export async function jsonToPython(jsonString, {
  allOptional = false,
  pydantic = false,
  typeName = 'AutoGenerated',
} = {}) {
  if (!jsonString || jsonString.trim() === '') {
    return { ok: true, code: '' }
  }

  let parsed
  try {
    parsed = JSON.parse(jsonString)
  } catch (e) {
    return { ok: false, error: 'JSON 解析失败：' + e.message }
  }

  const trimmedName = (typeName || '').trim()
  const isValidName = trimmedName === '' || PY_IDENT_RE.test(trimmedName)
  const safeName = isValidName ? (trimmedName || 'AutoGenerated') : 'AutoGenerated'
  const warning = isValidName
    ? undefined
    : `结构体名 "${trimmedName}" 不是合法 Python 标识符`

  try {
    const jsonInput = jsonInputForTargetLanguage('python')
    await jsonInput.addSource({ name: safeName, samples: [JSON.stringify(parsed)] })
    const inputData = new InputData()
    inputData.addInput(jsonInput)

    const rendererOptions = { 'just-types': true }
    if (pydantic) rendererOptions['pydantic-base-model'] = true
    if (allOptional) rendererOptions['python-version'] = '3.9'
    const extraOptions = {}
    if (allOptional) extraOptions.allPropertiesOptional = true

    const result = await quicktype({
      lang: 'python',
      inputData,
      rendererOptions,
      ...extraOptions,
    })
    return { ok: true, code: result.lines.join('\n'), warning }
  } catch (e) {
    return { ok: false, error: '生成失败：' + e.message }
  }
}
